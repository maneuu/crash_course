# Criando o dicionário com informações sobre uma pessoa
pessoa = {
    'first_name': 'John',
    'last_name': 'Silva',
    'age': 25,
    'city': 'São Paulo'
}

# Mostrando cada informação armazenada no dicionário
print(f"Primeiro nome: {pessoa['first_name']}")
print(f"Sobrenome: {pessoa['last_name']}")
print(f"Idade: {pessoa['age']} anos")
print(f"Cidade: {pessoa['city']}")